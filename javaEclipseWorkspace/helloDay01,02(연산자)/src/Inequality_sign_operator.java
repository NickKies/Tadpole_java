
public class Inequality_sign_operator {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c = 30;
				  
		// 대소비교 연산자 ==> 양변 크기 비교
		// == 같다, < 작다, >크다, <=작거나 같다, >=크거나 같다 != 같지 않다
				  
		System.out.println(a<b);  // true or false
		System.out.println(a>b);
		System.out.println(a+b<=c);  // 양변에 수식 가능
		System.out.println(a+b>=c);  // 산술연산자 부터 계산

	}

}
