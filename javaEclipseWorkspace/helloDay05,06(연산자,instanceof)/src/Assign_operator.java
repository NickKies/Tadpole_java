
public class Assign_operator {

	public static void main(String[] args) {
		int a, b;
		  a = 3;
		  b = 5;
		  
		  b = a;
		  System.out.println(b);
		  
		  a+=1;
		  System.out.println(a);
		  a/=2;
		  System.out.println(a);
		  a*=a;
		  System.out.println(a);  // a=2 이기 때문에 
		         // a = 2*2 == 4 

	}

}
